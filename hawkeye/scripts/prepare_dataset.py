"""
prepare_dataset.py — Download HAWKEYE dataset from Roboflow and fix structure.

Steps performed:
  1. Download project version in YOLOv8 format (train/valid/test already split)
  2. Rename valid/ → val/ (YOLO convention)
  3. Remap class name: 'Foreign Object' → 'fod'  (class index stays 0)
  4. Write config/dataset.yaml with absolute paths for Ubuntu training/eval

Usage (run from inside hawkeye/ directory):
    python scripts/prepare_dataset.py \\
        --api-key WCIxuet94KXWxzmAgRSQ \\
        --workspace durvas-workspace-ihhkq \\
        --project hawkeye-ap3a8 \\
        --version 1 \\
        --output /absolute/path/to/hawkeye/data/annotated

    # Example on Ubuntu:
    python scripts/prepare_dataset.py \\
        --api-key WCIxuet94KXWxzmAgRSQ \\
        --workspace durvas-workspace-ihhkq \\
        --project hawkeye-ap3a8 \\
        --version 1 \\
        --output /home/koushik-test/argusN/hawkeye/data/annotated

IMPORTANT: Always use an absolute path for --output. Relative paths silently
fail with the Roboflow SDK.
"""

import sys
import shutil
import argparse
from pathlib import Path


def download_dataset(api_key: str, workspace: str, project: str,
                     version: int, output_dir: str) -> Path:
    """
    Download dataset from Roboflow in YOLOv8 format.
    Returns the path where Roboflow placed the files.
    """
    try:
        from roboflow import Roboflow
    except ImportError:
        print("ERROR: roboflow package not installed. Run: pip install roboflow")
        sys.exit(1)

    output_dir = str(Path(output_dir).resolve())   # must be absolute
    print(f"Downloading to: {output_dir}")

    rf = Roboflow(api_key=api_key)
    proj = rf.workspace(workspace).project(project)
    dataset = proj.version(version).download("yolov8", location=output_dir)

    print(f"Download complete: {dataset.location}")
    return Path(dataset.location)


def fix_structure(dataset_root: Path) -> None:
    """
    Roboflow YOLOv8 download uses 'valid/' — rename to 'val/'.
    Works in-place on the downloaded folder.
    """
    for split_dir in ["images", "labels"]:
        src = dataset_root / split_dir / "valid"
        dst = dataset_root / split_dir / "val"
        if src.exists() and not dst.exists():
            src.rename(dst)
            print(f"  Renamed {split_dir}/valid → {split_dir}/val")
        elif dst.exists():
            print(f"  {split_dir}/val already exists — skipping rename")

    # Also rename at top level if Roboflow put a valid/ folder there
    top_valid = dataset_root / "valid"
    top_val   = dataset_root / "val"
    if top_valid.exists() and not top_val.exists():
        top_valid.rename(top_val)
        print("  Renamed top-level valid/ → val/")


def remap_classes(dataset_root: Path) -> None:
    """
    Roboflow may name the class 'Foreign Object'. Remap to 'fod'.
    Class index (0) stays the same — only the name in data.yaml changes.
    Label .txt files are untouched since they already use index 0.
    """
    # Find and update data.yaml or any yaml with class names
    yaml_files = list(dataset_root.glob("*.yaml")) + list(dataset_root.glob("data.yaml"))
    for yf in yaml_files:
        text = yf.read_text()
        if "Foreign Object" in text:
            text = text.replace("Foreign Object", "fod")
            yf.write_text(text)
            print(f"  Remapped class name in {yf.name}")


def write_dataset_yaml(dataset_root: Path, output_yaml: Path) -> None:
    """
    Write config/dataset.yaml with absolute paths.
    This is the file passed to YOLO val() and training.
    """
    train_path = dataset_root / "images" / "train"
    val_path   = dataset_root / "images" / "val"
    test_path  = dataset_root / "images" / "test"

    # Verify splits exist
    for name, path in [("train", train_path), ("val", val_path), ("test", test_path)]:
        if not path.exists():
            print(f"  WARNING: {name} split not found at {path}")

    content = f"""# HAWKEYE dataset — auto-generated by prepare_dataset.py
path: {dataset_root.resolve()}
train: images/train
val:   images/val
test:  images/test

nc: 1
names: ['fod']
"""

    output_yaml.parent.mkdir(parents=True, exist_ok=True)
    output_yaml.write_text(content)
    print(f"  Dataset YAML written → {output_yaml}")

    # Print split sizes
    for split, path in [("train", train_path), ("val", val_path), ("test", test_path)]:
        if path.exists():
            n = len(list(path.glob("*.jpg")) + list(path.glob("*.png")))
            print(f"    {split:5s}: {n} images")


def main():
    parser = argparse.ArgumentParser(
        description="Download HAWKEYE dataset from Roboflow and fix structure."
    )
    parser.add_argument("--api-key",   required=True,  help="Roboflow API key")
    parser.add_argument("--workspace", required=True,  help="Roboflow workspace slug")
    parser.add_argument("--project",   required=True,  help="Roboflow project slug")
    parser.add_argument("--version",   type=int, default=1, help="Dataset version (default: 1)")
    parser.add_argument(
        "--output", required=True,
        help="ABSOLUTE path to write dataset (e.g. /home/user/argusN/hawkeye/data/annotated)"
    )
    parser.add_argument(
        "--dataset-yaml", default="config/dataset.yaml",
        help="Where to write the dataset YAML (default: config/dataset.yaml)"
    )
    args = parser.parse_args()

    if not Path(args.output).is_absolute():
        print("ERROR: --output must be an absolute path. Roboflow silently fails with relative paths.")
        sys.exit(1)

    print("=" * 55)
    print("HAWKEYE — Prepare Dataset")
    print("=" * 55)

    # 1. Download
    print("\n[1/4] Downloading from Roboflow...")
    dataset_root = download_dataset(
        api_key=args.api_key,
        workspace=args.workspace,
        project=args.project,
        version=args.version,
        output_dir=args.output
    )

    # 2. Fix valid → val
    print("\n[2/4] Fixing directory structure...")
    fix_structure(dataset_root)

    # 3. Remap class names
    print("\n[3/4] Remapping class names...")
    remap_classes(dataset_root)

    # 4. Write dataset.yaml
    print("\n[4/4] Writing dataset YAML...")
    write_dataset_yaml(dataset_root, Path(args.dataset_yaml))

    print("\n" + "=" * 55)
    print("Dataset ready.")
    print(f"  Root      : {dataset_root}")
    print(f"  YAML      : {args.dataset_yaml}")
    print("\nNext step: copy YOLO weights and build PatchCore bank.")
    print("  cp ../yolofinetune/models/yolo/finetuned/best.pt models/yolo/finetuned/best.pt")
    print("  python scripts/build_patchcore_bank.py --images data/clean_frames --save models/patchcore/bank.pt")


if __name__ == "__main__":
    main()
